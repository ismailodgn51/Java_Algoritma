public class Instarctor {
    String name;
    String surname;
    String  depeartmant;

    public Instarctor(String name, String surname, String depeartmant) {
        this.name = name;
        this.surname = surname;
        this.depeartmant = depeartmant;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getDepeartmant() {
        return depeartmant;
    }

    public void setDepeartmant(String depeartmant) {
        this.depeartmant = depeartmant;
    }
}
